package scripts;

public class Strings {

	public static void main(String[] args) {
		String Str = "Jai Sadguru";
		//To find out the index value of character s
		System.out.println(Str.indexOf("S"));
		//To find out the character present at index value 4
		System.out.println(Str.charAt(4));
		//To print the substring present after index value 4
		System.out.println(Str.substring(4));

	}

}
